class EyeDeepfakeDataset(Dataset):
    def __init__(self, root_dir, split="train", transform=None):
        self.root_dir = Path(root_dir) / split
        self.transform = transform
        self.images = []
        self.labels = []

        # 0 = Real, 1 = Fake
        for label_idx, label_name in enumerate(["real", "fake"]):
            folder = self.root_dir / label_name
            if not folder.exists(): continue

            for img_path in folder.glob("*.jpg"):
                self.images.append(str(img_path))
                self.labels.append(label_idx)

        print(f"📁 {split.upper()} Seti Yüklendi -> Toplam: {len(self.images)} Göz (Real: {self.labels.count(0)}, Fake: {self.labels.count(1)})")

    def __len__(self):
        return len(self.images)

    def __getitem__(self, idx):
        img_path = self.images[idx]
        label = self.labels[idx]

        # Resmi aç ve RGB'ye çevir
        image = Image.open(img_path).convert("RGB")

        if self.transform:
            image = self.transform(image)

        return image, torch.tensor(label, dtype=torch.long)

