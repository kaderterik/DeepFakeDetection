def get_eye_bbox(landmarks, eye_indices, img_w, img_h, padding=0.25):
    xs = [landmarks[i].x * img_w for i in eye_indices]
    ys = [landmarks[i].y * img_h for i in eye_indices]
    x1, x2 = int(min(xs)), int(max(xs))
    y1, y2 = int(min(ys)), int(max(ys))
    pw = int((x2 - x1) * padding)
    ph = int((y2 - y1) * padding)
    return max(0, x1 - pw), max(0, y1 - ph), min(img_w, x2 + pw), min(img_h, y2 + ph)

def extract_eye_region(image_path, size=128, padding=0.25):
    img = cv2.imread(str(image_path))
    if img is None: return None

    img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    h, w = img.shape[:2]

    mp_image = mp.Image(image_format=mp.ImageFormat.SRGB, data=img_rgb)
    result = face_landmarker.detect(mp_image)

    if not result.face_landmarks: return None  # Yüz bulunamadı

    lm = result.face_landmarks[0]

    lx1, ly1, lx2, ly2 = get_eye_bbox(lm, LEFT_EYE, w, h, padding)
    rx1, ry1, rx2, ry2 = get_eye_bbox(lm, RIGHT_EYE, w, h, padding)

    left_eye  = img_rgb[ly1:ly2, lx1:lx2]
    right_eye = img_rgb[ry1:ry2, rx1:rx2]

    if left_eye.size == 0 or right_eye.size == 0: return None

    half = size // 2
    left_eye  = cv2.resize(left_eye, (half, size))
    right_eye = cv2.resize(right_eye, (half, size))
    combined  = np.concatenate([left_eye, right_eye], axis=1)

    return Image.fromarray(combined)

