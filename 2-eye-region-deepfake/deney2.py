def train_model(model, criterion, optimizer, num_epochs=10):
    since = time.time()

    best_model_wts = copy.deepcopy(model.state_dict())
    best_auc = 0.0
    history = {'train_loss': [], 'val_loss': [], 'train_auc': [], 'val_auc': []}

    for epoch in range(num_epochs):
        print(f"\n🔄 Epoch {epoch+1}/{num_epochs}")
        print("-" * 30)

        for phase in ['train', 'val']:
            if phase == 'train':
                model.train()  # Modeli eğitim moduna al
            else:
                model.eval()   # Modeli test moduna al (ağırlıkları kilitle)

            running_loss = 0.0
            all_preds = []
            all_labels = []

            # Progress bar (tqdm)
            for inputs, labels in tqdm(dataloaders[phase], desc=phase.upper(), leave=False):
                inputs = inputs.to(DEVICE)
                labels = labels.to(DEVICE)

                optimizer.zero_grad() # Önceki adımdan kalan hataları sıfırla

                with torch.set_grad_enabled(phase == 'train'):
                    outputs = model(inputs)
                    loss = criterion(outputs, labels)

                    # Softmax ile çıktıları olasılıklara (0.0 - 1.0) çevir
                    probs = F.softmax(outputs, dim=1)[:, 1]

                    if phase == 'train':
                        loss.backward()  # Geri yayılım (Öğrenme)
                        optimizer.step()

                running_loss += loss.item() * inputs.size(0)
                all_preds.extend(probs.cpu().detach().numpy())
                all_labels.extend(labels.cpu().numpy())

            # Tur (Epoch) Sonu Hesaplamaları
            epoch_loss = running_loss / dataset_sizes[phase]
            # Dengesiz veri setleri için Accuracy yerine ROC_AUC daha güvenilirdir
            epoch_auc = roc_auc_score(all_labels, all_preds)

            history[f'{phase}_loss'].append(epoch_loss)
            history[f'{phase}_auc'].append(epoch_auc)

            print(f"📊 {phase.upper()} -> Loss: {epoch_loss:.4f} | AUC: {epoch_auc:.4f}")

            # Eğer validation AUC skoru şimdiye kadarkinden iyiyse, modeli kaydet!
            if phase == 'val' and epoch_auc > best_auc:
                print(f"⭐ YENİ REKOR! (AUC: {best_auc:.4f} -> {epoch_auc:.4f}) Model Kaydediliyor...")
                best_auc = epoch_auc
                best_model_wts = copy.deepcopy(model.state_dict())

    time_elapsed = time.time() - since
    print(f"\n✅ EĞİTİM TAMAMLANDI! (Süre: {time_elapsed // 60:.0f}dk {time_elapsed % 60:.0f}sn)")
    print(f"🏆 En İyi Val AUC Skoru: {best_auc:.4f}")

    # En iyi öğrenmiş haliyle modeli geri döndür
    model.load_state_dict(best_model_wts)

    # En iyi modeli diske kaydet
    save_path = f"{CONFIG['results_dir']}/best_{model_name}.pth"
    torch.save(model.state_dict(), save_path)
    print(f"💾 Model diske kaydedildi: {save_path}")

    return model, history

