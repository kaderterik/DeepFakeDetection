def extract_frames_from_video(video_path, output_dir, video_id, label, n_frames=20):
    cap = cv2.VideoCapture(str(video_path))
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

    if total <= 0: return 0

    if total < n_frames:
        indices = set(range(total))
    else:
        indices = set(np.linspace(0, total - 1, n_frames, dtype=int))

    saved = 0
    current_frame = 0

    # cap.set() YERİNE DÜZ OKUMA (Yüzlerce kat daha hızlı)
    while True:
        ret, frame = cap.read()
        if not ret: break  # Video bitti

        if current_frame in indices:
            fname = f"{label}_{video_id}_{current_frame:05d}.jpg"
            cv2.imwrite(os.path.join(output_dir, fname), frame)
            saved += 1

        current_frame += 1
        # Tüm istediğimiz kareleri aldıysak videonun kalanını okumaya gerek yok
        if saved >= n_frames: break

    cap.release()
    return saved

def get_video_files(directories, extensions):
    videos = []
    if isinstance(directories, str): directories = [directories]
    for d in directories:
        for ext in extensions:
            videos.extend(Path(d).rglob(f"*{ext}"))
    return sorted(videos)

def video_level_split(video_list, train_r=0.70, val_r=0.15, seed=42, max_limit=500):
    random.seed(seed)
    vids = list(video_list)
    random.shuffle(vids)
    if len(vids) > max_limit: vids = vids[:max_limit]
    n = len(vids)
    t = int(n * train_r)
    v = int(n * val_r)
    return vids[:t], vids[t:t+v], vids[t+v:]

